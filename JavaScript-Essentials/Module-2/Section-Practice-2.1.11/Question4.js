// Declare the variables of Number Primitive Type
let number_var1 = 5;
let number_var2 = 7;
let sum_num = number_var1 + number_var2;
console.log(`${sum_num} : [${typeof sum_num}]`);

// Declare the variables of String Primitive Type
let string_var1 = "Hello";
let string_var2 = "World";
let string_sum = string_var1 + string_var2;
console.log(`${string_sum} : [${typeof string_sum}]`);

// Declare the variables of BigInt Primitive Type
let bigInt_var1 = 9n;
let bigInt_var2 = 6n;
let bigInt_sum = bigInt_var1 + bigInt_var2;
console.log(`${bigInt_sum} : [${typeof bigInt_sum}]`);

// Declare the variables of Boolean primitive Type
let boolean_var1 = true;
let boolean_var2 = true;
let boolean_sum = boolean_var1 + boolean_var2
console.log(`${boolean_sum} : [${typeof boolean_sum}]`);

// Creating undefined variables
let undef_var1 = undefined;
let undef_var2 = undefined;
let undef_sum = undef_var1 + undef_var2;
console.log(`${undef_sum} : [${typeof undef_sum}]`);