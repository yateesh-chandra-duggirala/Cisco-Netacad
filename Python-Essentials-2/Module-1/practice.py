# By using "import" keyword, we can use a specific module in our python Script.
# Also, we can import 2 modules with a seperated comma between them
import math, sys

# Print Mathematical Sin Function
print(math.sin(90))

# Printing Logarithmic value to the base 10
print(math.log10(100))

# Printing Pi from Mathematical Module - Its value is Mathematical PI : 3.14
print(math.pi)

print(math.exp(1))